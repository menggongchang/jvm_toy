package math

import (
	"jvmgo/instructions/base"
	"jvmgo/rtda"
)

type ISHL struct {
	base.NoOperandsInstruction
}

func (self *ISHL) Execute(frame *rtda.Frame) {
	stack := frame.OperandStack()
	v2 := stack.PopInt()
	v1 := stack.PopInt()
	s := uint32(v2) & 0x1f
	v1 = v1 << s
	stack.PushInt(v1)
}

